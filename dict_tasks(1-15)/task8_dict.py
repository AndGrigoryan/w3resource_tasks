#   Write a Python script to merge two Python dictionaries.
#
#---------------------------------------------------------------------------------------------

md1 = {'a': 1, 'b': 2, 'c': 3}
md2 = {1: 'a', 2: 'b', 3: 'c'}

md3 = md1.copy()
md3.update(md2)

print(md3)

